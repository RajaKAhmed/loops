// Go program to illustrate the usage of three component loop. Similar to Java and C.
// i is initialized to 1, condition is set to be less than 6 and i is incremented.
// As i increments it prints the value of i followed by Missisipi.
package main

import (
	"fmt"
)

func main() {
	for i := 1; i < 5; i++ {
		fmt.Println("For Loop implementation in Golang", i)
	}

}
