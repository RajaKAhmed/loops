// Java program to illustrate enhanced for loop two Implementations of the same loop.
public class LoopThroughArrayB
{
	public static void main(String args[])
	{
		String array[] = {"Ali", "Baba", "Derek", "Jeter"};

       /* for loop for same function */

        for (int i = 0; i < array.length; i++)
        {
            System.out.println(array[i]);
        }
	}
}
