// Java program to illustrate enhanced for loop two Implementations of the same loop.
public class LoopThroughArrayA
{
	public static void main(String args[])
	{
		String array[] = {"Ali", "Baba", "Derek", "Jeter"};

		//enhanced for loop
		for (String x:array)
		{
			System.out.println(x);
		}

	}
}
